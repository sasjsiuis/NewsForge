// LocalStorage key for saving/recalling current rotator index
export const STORAGE_ROTATOR_INDEX_KEY = "gemini_rotator_index";

/**
 * Gets the stored rotator index or falls back to 0.
 */
export function getSavedRotatorIndex(): number {
  try {
    const val = localStorage.getItem(STORAGE_ROTATOR_INDEX_KEY);
    if (val) {
      const idx = parseInt(val, 10);
      if (!isNaN(idx) && idx >= 0 && idx < SYSTEM_API_KEYS.length) {
        return idx;
      }
    }
  } catch (e) {
    console.warn("localStorage read failed", e);
  }
  return 0;
}

/**
 * Saves a given rotator index to localStorage.
 */
export function saveRotatorIndex(index: number): void {
  try {
    localStorage.setItem(STORAGE_ROTATOR_INDEX_KEY, String(index));
  } catch (e) {
    console.warn("localStorage write failed", e);
  }
}
